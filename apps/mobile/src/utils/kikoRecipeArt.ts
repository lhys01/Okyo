export function normalizeKikoRecipeStepText(value: string) {
  return value
    .replace(/½/g, ' half ')
    .replace(/¼/g, ' quarter ')
    .replace(/¾/g, ' three quarters ')
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, ' ')
    .split(/\s+/)
    .filter(Boolean)
    .map((token) => TOKEN_ALIASES[token] ?? token)
    .join(' ')
    .trim();
}

export function getKikoRecipeArtFileNameForStep(stepText: string): KikoRecipeArtFileName | null {
  const normalizedStep = normalizeKikoRecipeStepText(stepText);
  if (!normalizedStep) {
    return null;
  }

  const matchingRule = MATCH_RULES.find((rule) => matchesRule(normalizedStep, rule));
  return matchingRule?.fileName ?? null;
}

function matchesRule(normalizedStep: string, rule: KikoRecipeArtMatchRule) {
  if (rule.none?.some((term) => hasTerm(normalizedStep, term))) {
    return false;
  }

  if (rule.phrases?.some((phrase) => hasTerm(normalizedStep, phrase))) {
    return true;
  }

  return Boolean(rule.groups?.length) && rule.groups!.every(
    (alternatives) => alternatives.some((term) => hasTerm(normalizedStep, term)),
  );
}

function hasTerm(normalizedStep: string, term: string) {
  const normalizedTerm = normalizeKikoRecipeStepText(term);
  return ` ${normalizedStep} `.includes(` ${normalizedTerm} `);
}
