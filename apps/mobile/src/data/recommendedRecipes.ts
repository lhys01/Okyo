export type RecommendationRecipe = Recipe & {
  category: RecommendationCategory;
  mealTimes: MealTime[];
  blurb: string;
  tint: string;
  emoji: string;
};

type RecommendationSpec = {
  id: string;
  title: string;
  category: RecommendationCategory;
  mealTimes: MealTime[];
  difficulty: Recipe['difficulty'];
  prep: number;
  cook: number;
  servings: number;
  homemadeCost: number;
  blurb: string;
  ingredients: Array<[quantity: string, name: string]>;
  steps: Array<{ text: string; time?: string; cue?: string }>;
  substitutions: string[];
  equipment: string[];
  imageKey?: SampleFoodImageKey;
  // Pexels photo ID for a per-recipe image. License: Pexels License (free commercial use, no attribution required).
  // URL resolved at build time: https://images.pexels.com/photos/{id}/pexels-photo-{id}.jpeg?auto=compress&cs=tinysrgb&w=600
  pexelsId?: number;
  pantryNote: string;
};

function buildRecommendation(spec: RecommendationSpec): RecommendationRecipe {
  const ingredients: RecipeIngredient[] = spec.ingredients.map(([quantity, name]) => ({ name, quantity }));
  const structuredSteps: RecipeStep[] = spec.steps.map((step) => ({
    text: step.text,
    timeEstimate: step.time,
    visualCue: step.cue,
  }));
  const art = categoryArt[spec.category];

  return {
    id: `rec-${spec.id}`,
    scanResultId: `rec-${spec.id}`,
    imageStatus: 'ready',
    // Image priority: (1) local bundled asset (populated by scripts/download-food-images.sh),
    // (2) Pexels CDN, (3) category placeholder.
    imageUrl: (() => {
      const localAsset = recipeAssets[spec.id];
      if (localAsset !== undefined) {
        try {
          return Image.resolveAssetSource(localAsset).uri;
        } catch {
          // fall through
        }
      }
      if (spec.pexelsId) {
        return `https://images.pexels.com/photos/${spec.pexelsId}/pexels-photo-${spec.pexelsId}.jpeg?auto=compress&cs=tinysrgb&w=600`;
      }
      return getSampleFoodImageUrl(spec.imageKey ?? getCategoryImageKey(spec.category));
    })(),
    title: spec.title,
    mode: 'Restaurant Copy',
    description: spec.blurb,
    prepTimeMinutes: spec.prep,
    cookTimeMinutes: spec.cook,
    totalTimeMinutes: spec.prep + spec.cook,
    servings: spec.servings,
    difficulty: spec.difficulty,
    skillLevel: spec.difficulty,
    estimatedHomemadeCost: spec.homemadeCost,
    estimatedSavings: 0,
    ingredients,
    steps: structuredSteps.map((step) => step.text),
    structuredSteps,
    substitutions: spec.substitutions,
    pantryNote: spec.pantryNote,
    confidenceNote: 'Okyo recommended recipe. Cost is a home-kitchen estimate, not a restaurant price.',
    equipment: spec.equipment,
    bestFor: spec.blurb,
    category: spec.category,
    mealTimes: spec.mealTimes,
    blurb: spec.blurb,
    tint: art.tint,
    emoji: art.emoji,
  };
}

function getCategoryImageKey(category: RecommendationCategory): SampleFoodImageKey {
  switch (category) {
    case 'Breakfast':
    case 'Smoothies':
      return 'breakfast';
    case 'High Protein':
    case 'Bowls':
    case 'Dinner Ideas':
      return 'bowl';
    case 'Pasta':
      return 'pasta';
    case 'Salads':
      return 'salad';
    case 'Burgers & Sandwiches':
    case 'Pizza':
    case 'Snacks':
      return 'burger';
    case 'Desserts':
      return 'dessert';
    default:
      return 'bowl';
  }
}

